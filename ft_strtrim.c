#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int		i;
	int		start;
	int		end;
	char	*cpy;

	if (s1 == NULL || set == NULL)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	cpy = malloc((end - start + 1) * sizeof(char));
	if (!cpy)
		return (NULL);
	i = 0;
	while (start < end)
		cpy[i++] = s1[start++];
	cpy[i] = '\0';
	return (cpy);
}
